"""Unit conversion and physical constants.
Internal units:
    Energy: kcal/mol
    Length: angstrom (A)
Atomic units:
    Energy: hartree (Eh)
    Length: bohr (a0)
"""

# Unit ratios
KCAL_TO_JOULE = 4.184e+03
ANGSTROM_TO_METER = 1e-10

# 2018 CODATA
CODATA18_AVOGADRO_CONSTANT = 6.02214076e+23
CODATA18_HARTREE_TO_JOULE = 4.3597447222071e-18
CODATA18_EV_TO_JOULE = 1.602176634e-19
CODATA18_BOHR_TO_METER = 5.29177210903e-11

CODATA18_HARTREE_TO_KCAL = CODATA18_HARTREE_TO_JOULE / KCAL_TO_JOULE * CODATA18_AVOGADRO_CONSTANT
CODATA18_BOHR_TO_A = CODATA18_BOHR_TO_METER / ANGSTROM_TO_METER
CODATA18_HARTREE_TO_EV = CODATA18_HARTREE_TO_JOULE / CODATA18_EV_TO_JOULE

# AmberTools Constants
AMBER_HARTREE_TO_EV = 27.21
AMBER_EV_TO_KCAL= 23.061
AMBER_HARTREE_TO_KCAL = AMBER_HARTREE_TO_EV * AMBER_EV_TO_KCAL
AMBER_BOHR_TO_A = 0.529177249

# ORCA
ORCA_BOHR_TO_A = 0.5291772083
